.globl _reset	@ 输出_reset函数给别的文件调用

/*
 * brief	复位中断处理函数
 */
_reset:
	/*
	 * \brief	链接文件link.ld中已配置将所有代码段都这个地址开始放置
	 */
	mov sp, #0x8000	@ 设置栈顶地址
	bl main			@ 跳转到C语言的main函数
